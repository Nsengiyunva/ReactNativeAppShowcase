// ======================
// BRANCHES
// ======================
// - Every task will require a new branch
// - Always make sure that your local branch is in line with the remote to avoid conflicts
// - Always rebase off maintenance branch before creating a Pull Request.
// - Make sure that your Pull Request point to maintenance branch as the base branch and not master.

// ======================
// COMPONENTS
// ======================

// - Components with state will be created as class components.
// - Components should be put in their own directories
// - Component filenames should be written with Titlecase e.g ExampleComponent
// - Components should have a proptypes object with relevant props listed therein.

// ======================
// STYLES
// ======================

// - Styles should be declared in separate files in the same folder as the components themselves.
// - They should be written as normal Javascript files with a style object inside.

// ======================
// VARIABLES
// ======================

// - Variable names should be descriptive and short. i.e getNames and not getNamesfromDatabase.
// - variable names should be camelCased i.e camelCase not camelCASE or CamelCase.

// ======================
// FUNCTIONS
// ======================

// - Functions should be commented where needed for easy maintenance.
// - Arrow functions should be used rather than general functions like below.
// - ES6 functions should be written with camelCased function names.

// => THIS
// const oneFunction = () => {
//     // block of code
// };

// => NOT
// function oneFunction() {
//     // block of code.
// };
